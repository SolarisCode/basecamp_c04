#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb < 10 && nb >= 0)
	{
		nb += 48;
		write(1, &nb, 1);
	}
	else if (nb > 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		write(1, "-", 1);
		ft_putnbr(nb * -1);
	}
}

